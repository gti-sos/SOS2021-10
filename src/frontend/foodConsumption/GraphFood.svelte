    async function getData(){
        console.log("Fetching data...");
        const res = await fetch("/foodconsumption-stats");
        if(res.ok){
            console.log("Ok.");
            const json = await res.json();
            data = json;
            console.log(`We have received ${data.length} data points.`);
        }else{
            console.log("Error!");
        }
    }   
    
